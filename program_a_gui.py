def get_icon_path():
    """从 Base64 数据创建临时图标文件，返回路径"""
    try:
        temp_dir = tempfile.gettempdir()
        icon_path = os.path.join(temp_dir, "truenas_tool_icon.ico")
        if not os.path.exists(icon_path):
            with open(icon_path, 'wb') as f:
                f.write(base64.b64decode(ICON_BASE64))
        return icon_path
    except Exception:
        return None
