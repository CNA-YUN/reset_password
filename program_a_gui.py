    def _browse_input(self):
        filename = filedialog.askopenfilename(
            title="选择明文 API Key 文件",
            filetypes=[("Text files", "*.txt"), ("All files", "*.*")]
        )
        if filename:
            self.input_file.set(filename)

    def _log(self, message):
        """向日志区域追加信息"""
        self.log_text.config(state=tk.NORMAL)
        self.log_text.insert(tk.END, message + "\n")
        self.log_text.see(tk.END)
        self.log_text.config(state=tk.DISABLED)
        self.root.update_idletasks()

    @classmethod
    def _derive_key(cls) -> bytes:
        """使用内部种子派生 Fernet 密钥，无需用户输入密码"""
        kdf = PBKDF2HMAC(
            algorithm=hashes.SHA256(),
            length=32,
            salt=cls.INTERNAL_SALT,
            iterations=480000
        )
        return base64.urlsafe_b64encode(kdf.derive(cls.INTERNAL_SEED.encode('utf-8')))

    def _generate_config(self):
        """核心加密与生成逻辑"""
        in_file = self.input_file.get().strip()
        ip_addr = self.truenas_ip.get().strip()
        out_file = CONFIG_FILE

        # 1. 基础校验
        if not os.path.isfile(in_file):
            messagebox.showerror("错误", f"找不到输入文件:\n{in_file}")
            return

        if not ip_addr:
            messagebox.showwarning("警告", "TrueNAS IP 地址不能为空！")
            return

        # 去除可能误输入的协议前缀，统一拼接
        clean_ip = ip_addr.replace("http://", "").replace("https://", "").rstrip("/")
        api_url = f"http://{clean_ip}/api/v2.0"

        # 2. 读取明文 Key
        self._log(f"[*] 正在读取明文文件: {in_file}")
        try:
            with open(in_file, 'r', encoding='utf-8') as f:
                api_key = f.read().strip()
            if not api_key:
                raise ValueError("文件内容为空")
        except Exception as e:
            self._log(f"[❌] 读取文件失败: {e}")
            messagebox.showerror("读取失败", str(e))
            return

        # 3. 加密过程
        self._log("[*] 正在使用内置密钥派生加密参数 (AES-256)...")
        self.gen_btn.config(state=tk.DISABLED)
        self.root.update()

        try:
            key = self._derive_key()
            fernet = Fernet(key)
            self._log("[*] 正在加密 API Key...")
            encrypted_data = fernet.encrypt(api_key.encode('utf-8'))
        except Exception as e:
            self._log(f"[❌] 加密过程发生致命错误: {e}")
            messagebox.showerror("加密失败", str(e))
            self.gen_btn.config(state=tk.NORMAL)
            return

        # 4. 写入 INI
        self._log(f"[*] 正在写入配置文件: {out_file}")
        config = configparser.ConfigParser()
        config['TrueNAS_Config'] = {
            'api_url': api_url,
            'encrypted_api_key': encrypted_data.decode('utf-8'),
            'salt': base64.b64encode(self.INTERNAL_SALT).decode('utf-8'),
            'iterations': '480000'
        }

        try:
            with open(out_file, 'w', encoding='utf-8') as configfile:
                config.write(configfile)
            self._log("[✅] 成功！加密配置已安全保存。")
            self._log("-" * 50)
            messagebox.showinfo("成功", f"加密配置生成成功！\n\n保存位置:\n{out_file}")
        except Exception as e:
            self._log(f"[❌] 写入 INI 文件失败: {e}")
            messagebox.showerror("写入失败", str(e))
        finally:
            self.gen_btn.config(state=tk.NORMAL)


